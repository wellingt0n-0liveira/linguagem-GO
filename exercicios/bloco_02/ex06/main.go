// 6. Faça um programa que exiba a seguinte
// receita de bolo de chocolate.
// Exiba um item por vez, conforme a pessoa aperte “enter”.

package main

import "fmt"

func main() {

	var ok string

	fmt.Println("Ingredientes:")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 2 xícaras de farinha de trigo")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1 xícara de açúcar")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1/2 xícara de cacau em pó")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1 colher de chá de fermento em pó")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1 colher de chá de bicarbonato de sódio")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1/2 colher de chá de sal")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 2 ovos")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1 xícara de leite")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1/2 xícara de óleo vegetal")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 2 colheres de chá de extrato de baunilha")
	fmt.Scanf("%s", ok)

	fmt.Println("	- 1 xícara de água fervente")
	fmt.Scanf("%s", ok)

	fmt.Println("Modo de preparo:")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Pré-aqueça o forno a 180°C. Unte uma forma de bolo com manteiga e farinha, ou forre-a com papel manteiga.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Em uma tigela grande, peneire a farinha de trigo, o açúcar, o cacau em pó, o fermento em pó, o bicarbonato de sódio e o sal. Misture bem.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Em outra tigela, bata os ovos levemente. Adicione o leite, o óleo vegetal e o extrato de baunilha. Misture bem.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Despeje os ingredientes líquidos na mistura de ingredientes secos e mexa até que tudo esteja bem combinado.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Adicione a água fervente à massa e misture até que a massa fique homogênea. A massa ficará líquida, mas é normal.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Despeje a massa na forma preparada e leve ao forno pré-aquecido.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Asse por cerca de 30-35 minutos, ou até que um palito inserido no centro do bolo saia limpo.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Retire o bolo do forno e deixe esfriar na forma por alguns minutos. Em seguida, transfira para uma grade de resfriamento para esfriar completamente.")
	fmt.Scanf("%s", ok)

	fmt.Println("	- Depois de esfriar, você pode servir o bolo simples ou decorá-lo com cobertura de sua preferência, como ganache de chocolate ou glacê de chocolate.")
	fmt.Scanf("%s", ok)

}
